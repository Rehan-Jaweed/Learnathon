#this program prints area of the rectangle

# Taking input from the user
length = float(input("Enter the length of the rectangle in cm : "))
width = float(input("Enter the width of the rectangle in cm : "))

# Calculating are of the rectangle
area = length * width

# Displaying results
print("\nResults :")
print("Area of the rectangle is : {:.2f}".format(area),"cm square units.")
